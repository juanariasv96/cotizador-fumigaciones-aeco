-- Mantener fecha_actualizacion al día en cada cambio (cotizaciones y
-- configuración de la empresa). La función se crea PRIMERO: los triggers
-- que la usan deben ir después, o Postgres se queja de que no existe.
create or replace function public.actualizar_fecha_actualizacion()
returns trigger
language plpgsql
as $$
begin
  new.fecha_actualizacion = now();
  return new;
end;
$$;

drop trigger if exists trg_cotizaciones_fecha_actualizacion on public.cotizaciones;
create trigger trg_cotizaciones_fecha_actualizacion
  before update on public.cotizaciones
  for each row execute function public.actualizar_fecha_actualizacion();

drop trigger if exists trg_configuracion_fecha_actualizacion on public.configuracion_empresa;
create trigger trg_configuracion_fecha_actualizacion
  before update on public.configuracion_empresa
  for each row execute function public.actualizar_fecha_actualizacion();


-- ---------------------------------------------------------------------
-- 2. AUTO-CREAR PERFIL AL REGISTRAR UN USUARIO NUEVO
-- ---------------------------------------------------------------------
-- Cuando crees un usuario nuevo en Authentication > Users, este trigger
-- le crea automáticamente su fila en perfiles_usuario con rol 'vendedor'
-- por defecto. Entra a la tabla perfiles_usuario y cambia el rol a
-- 'dueño', 'tecnico' o 'asistente' según corresponda a cada persona.

create or replace function public.manejar_usuario_nuevo()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.perfiles_usuario (id, nombre, rol, activo)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'nombre', split_part(new.email, '@', 1)),
    'vendedor',
    true
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists trg_usuario_nuevo on auth.users;
create trigger trg_usuario_nuevo
  after insert on auth.users
  for each row execute function public.manejar_usuario_nuevo();


-- ---------------------------------------------------------------------
-- 3. FUNCIÓN AUXILIAR: ¿el usuario actual es dueño?
-- ---------------------------------------------------------------------
create or replace function public.es_dueno()
returns boolean
language sql
stable
as $$
  select exists (
    select 1 from public.perfiles_usuario
    where id = auth.uid() and rol = 'dueño' and activo = true
  );
$$;


-- ---------------------------------------------------------------------
-- 4. ROW LEVEL SECURITY
-- ---------------------------------------------------------------------

alter table public.clientes enable row level security;
alter table public.cotizaciones enable row level security;
alter table public.perfiles_usuario enable row level security;
alter table public.registro_accesos enable row level security;
alter table public.configuracion_empresa enable row level security;

-- clientes: cualquier usuario autenticado ve, crea, actualiza y borra
-- (trabajo en equipo — todos comparten la misma cartera de clientes).
drop policy if exists "clientes_select_equipo" on public.clientes;
create policy "clientes_select_equipo" on public.clientes
  for select to authenticated using (true);

drop policy if exists "clientes_insert_equipo" on public.clientes;
create policy "clientes_insert_equipo" on public.clientes
  for insert to authenticated with check (true);

drop policy if exists "clientes_update_equipo" on public.clientes;
create policy "clientes_update_equipo" on public.clientes
  for update to authenticated using (true) with check (true);

drop policy if exists "clientes_delete_equipo" on public.clientes;
create policy "clientes_delete_equipo" on public.clientes
  for delete to authenticated using (true);

-- cotizaciones: mismo criterio de equipo compartido (incluye actualizar
-- estado y eliminar, que ya son funciones existentes del cotizador).
drop policy if exists "cotizaciones_select_equipo" on public.cotizaciones;
create policy "cotizaciones_select_equipo" on public.cotizaciones
  for select to authenticated using (true);

drop policy if exists "cotizaciones_insert_equipo" on public.cotizaciones;
create policy "cotizaciones_insert_equipo" on public.cotizaciones
  for insert to authenticated with check (true);

drop policy if exists "cotizaciones_update_equipo" on public.cotizaciones;
create policy "cotizaciones_update_equipo" on public.cotizaciones
  for update to authenticated using (true) with check (true);

drop policy if exists "cotizaciones_delete_equipo" on public.cotizaciones;
create policy "cotizaciones_delete_equipo" on public.cotizaciones
  for delete to authenticated using (true);

-- perfiles_usuario: cada quien ve su propio perfil; el dueño ve todos.
-- No se permite insert/update/delete desde la app (se administra en el
-- dashboard de Supabase o vía el trigger de usuario nuevo).
drop policy if exists "perfiles_select_propio" on public.perfiles_usuario;
create policy "perfiles_select_propio" on public.perfiles_usuario
  for select to authenticated using (id = auth.uid());

drop policy if exists "perfiles_select_dueno" on public.perfiles_usuario;
create policy "perfiles_select_dueno" on public.perfiles_usuario
  for select to authenticated using (public.es_dueno());

-- registro_accesos: CUALQUIERA puede insertar (incluso un login fallido,
-- donde todavía no hay sesión autenticada, cae bajo el rol "anon"). Solo
-- el dueño puede leer o borrar la bitácora.
drop policy if exists "accesos_insert_publico" on public.registro_accesos;
create policy "accesos_insert_publico" on public.registro_accesos
  for insert to anon, authenticated with check (true);

drop policy if exists "accesos_select_dueno" on public.registro_accesos;
create policy "accesos_select_dueno" on public.registro_accesos
  for select to authenticated using (public.es_dueno());

drop policy if exists "accesos_delete_dueno" on public.registro_accesos;
create policy "accesos_delete_dueno" on public.registro_accesos
  for delete to authenticated using (public.es_dueno());

-- configuracion_empresa: todo el equipo autenticado LEE la misma
-- configuración (logo, datos de contacto, costos base de la
-- calculadora); solo el dueño puede modificarla, para que nadie cambie
-- sin querer los precios base de todos.
drop policy if exists "config_select_equipo" on public.configuracion_empresa;
create policy "config_select_equipo" on public.configuracion_empresa
  for select to authenticated using (true);

drop policy if exists "config_update_dueno" on public.configuracion_empresa;
create policy "config_update_dueno" on public.configuracion_empresa
  for update to authenticated using (public.es_dueno()) with check (public.es_dueno());

drop policy if exists "config_insert_dueno" on public.configuracion_empresa;
create policy "config_insert_dueno" on public.configuracion_empresa
  for insert to authenticated with check (public.es_dueno());


-- ---------------------------------------------------------------------
-- 5. PERMISOS BASE (GRANT)
-- ---------------------------------------------------------------------
-- En Postgres, RLS solo FILTRA filas; antes de eso, el rol necesita el
-- permiso base sobre la tabla o Postgres rechaza con "permission denied
-- for table X" sin siquiera llegar a evaluar las políticas. Este era el
-- paso que faltaba: las políticas ya estaban bien, pero ningún rol tenía
-- concedido el permiso de base para usarlas.

grant usage on schema public to authenticated, anon;

grant select, insert, update, delete on public.clientes        to authenticated;
grant select, insert, update, delete on public.cotizaciones    to authenticated;
grant select                          on public.perfiles_usuario to authenticated;
grant select, delete                  on public.registro_accesos to authenticated;
grant insert                          on public.registro_accesos to authenticated, anon;
grant select, insert, update          on public.configuracion_empresa to authenticated;


