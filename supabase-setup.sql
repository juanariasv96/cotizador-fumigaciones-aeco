-- Clientes de la empresa (compartidos entre todo el equipo)
create table if not exists public.clientes (
  id               uuid primary key default gen_random_uuid(),
  nombre           text not null,
  telefono         text,
  email            text,
  direccion        text,
  contacto         text,
  zona             text,               -- Monterrey / Guadalupe / Escobedo / San Nicolás / Santa Catarina / Apodaca / San Pedro / García
  categoria        text,               -- Residencial / Empresarial / Industrial
  creado_por       uuid references auth.users(id),
  fecha_creacion   timestamptz not null default now()
);

-- Cotizaciones. "datos" guarda el objeto JSON completo tal como lo arma
-- hoy el formulario (plagas, áreas, póliza, precios, IVA, recomendaciones,
-- etc.) para no perder ningún detalle; las columnas sueltas son solo para
-- poder filtrar/ordenar rápido sin tener que leer todo el JSON.
create table if not exists public.cotizaciones (
  id                  uuid primary key default gen_random_uuid(),
  folio               bigint generated by default as identity unique,
  cliente_id          uuid references public.clientes(id),
  datos               jsonb not null,
  estado              text not null default 'Pendiente'
                       check (estado in ('Pendiente', 'Cerrada', 'Perdida')),
  vendedor            text,
  creado_por          uuid references auth.users(id),
  fecha_creacion      timestamptz not null default now(),
  fecha_actualizacion timestamptz not null default now()
);

-- Un perfil por usuario de Supabase Auth (rol y estatus del equipo)
create table if not exists public.perfiles_usuario (
  id       uuid primary key references auth.users(id) on delete cascade,
  nombre   text,
  rol      text not null default 'vendedor'
           check (rol in ('dueño', 'vendedor', 'tecnico', 'asistente')),
  activo   boolean not null default true
);

-- Bitácora de inicios de sesión (exitosos y fallidos)
create table if not exists public.registro_accesos (
  id            uuid primary key default gen_random_uuid(),
  usuario_id    uuid references auth.users(id),
  email         text not null,
  fecha_hora    timestamptz not null default now(),
  exitoso       boolean not null,
  ip_origen     text,   -- el navegador no expone la IP pública real; queda null salvo que se agregue un servicio externo
  user_agent    text
);

